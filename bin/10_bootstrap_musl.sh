#!/bin/bash
# SmechOS independent userland bootstrap -- Stage 0/1 (see MUSL_BOOTSTRAP_PLAN.md)
#
# Compiles musl libc from source into the target, using the build host's
# Clang/LLVM (apt-installed tooling, not compiled from source itself) instead
# of GCC/glibc. This replaces the old approach of copying a host's existing
# glibc-linked /usr/lib64 out of /mnt/kaymium_sovereign.
#
# Wired into build_order.txt as the first Phase 1 step. Not yet test-run --
# see MUSL_BOOTSTRAP_PLAN.md for the open question about whether bash should
# also be recompiled against musl in a later revision.
set -e

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
DEPLOY_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
SOURCES_DIR="$DEPLOY_ROOT/essentials/sources"

export SMECH_TARGET="${SMECH_TARGET:-/mnt/smechos_build_root}"

MUSL_VERSION="1.2.5"
MUSL_TARBALL="musl-${MUSL_VERSION}.tar.gz"
MUSL_URL="https://musl.libc.org/releases/${MUSL_TARBALL}"

echo "[+] --- STAGE 0: Verifying host Clang/LLVM toolchain ---"
if ! command -v clang >/dev/null 2>&1; then
    echo "Error: clang not found on host. Install it first (apt-get install clang lld llvm)."
    exit 1
fi
echo "[+] Host clang: $(clang --version | head -1)"

echo "[+] --- STAGE 1: Compiling musl ${MUSL_VERSION} ---"
if [ ! -f "$SOURCES_DIR/$MUSL_TARBALL" ]; then
    echo "[+] Downloading musl ${MUSL_VERSION}..."
    wget -O "$SOURCES_DIR/$MUSL_TARBALL" "$MUSL_URL"
fi

rm -rf /tmp/smechos_build/musl-${MUSL_VERSION}
mkdir -p /tmp/smechos_build/musl-${MUSL_VERSION}
tar -xf "$SOURCES_DIR/$MUSL_TARBALL" -C /tmp/smechos_build/musl-${MUSL_VERSION} --strip-components=1
cd /tmp/smechos_build/musl-${MUSL_VERSION}

# musl's own configure understands CC=clang directly -- no separate
# cross-toolchain bootstrap pass is needed the way glibc/GCC would require.
CC=clang ./configure --prefix=/usr
make -j"$(nproc)"
sudo make DESTDIR="$SMECH_TARGET" install

# musl ships its own compiler-wrapper generator (musl-gcc wrapper script,
# templated from a .specs file). Generate a "musl-clang" equivalent so later
# stages can compile against this musl install without re-deriving the
# include/lib paths every time.
WRAPPER_PATH="$SMECH_TARGET/usr/bin/musl-clang"
sudo tee "$WRAPPER_PATH" > /dev/null <<EOF
#!/bin/sh
# Generated by 10_bootstrap_musl.sh -- wraps clang to target this musl install.
exec clang --target=x86_64-linux-musl \\
    -isystem "$SMECH_TARGET/usr/include" \\
    -B"$SMECH_TARGET/usr/lib" \\
    -L"$SMECH_TARGET/usr/lib" \\
    -Wl,-dynamic-linker,"$SMECH_TARGET/usr/lib/libc.so" \\
    "\$@"
EOF
sudo chmod +x "$WRAPPER_PATH"

echo "[+] musl ${MUSL_VERSION} installed. Wrapper at $WRAPPER_PATH"
echo "[+] STAGE 0/1 COMPLETE"
